public class ForLoopExample {

    /**
     * @param args the command line arguments
     */
    public static void main(String[] args) {
        
        //initialize the loop
        
        for(int i =10; i>1; i--){
            System.out.println("The value of i is " +i);
            
        }
    }
    
}
